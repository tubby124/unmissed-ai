#!/usr/bin/env node
/**
 * write-scenario-results.mjs
 *
 * Reads .test-artifacts/call-scenarios.json and writes a dated summary to the
 * Obsidian vault wiki. Only run locally — never in CI.
 *
 * Usage: node scripts/write-scenario-results.mjs
 * Or via: npm run test:scenarios:report
 */

import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ARTIFACT = path.resolve(__dirname, '../.test-artifacts/call-scenarios.json')
const VAULT_PATH = '/Users/owner/Downloads/Obsidian Vault/wiki/unmissed/call-scenario-test-results.md'

if (!fs.existsSync(ARTIFACT)) {
  console.error('[write-scenario-results] No artifact found at', ARTIFACT)
  console.error('Run: npm run test:scenarios first')
  process.exit(1)
}

const report = JSON.parse(fs.readFileSync(ARTIFACT, 'utf8'))

const date = new Date(report.date).toLocaleString('en-CA', {
  timeZone: 'America/Regina',
  year: 'numeric', month: '2-digit', day: '2-digit',
  hour: '2-digit', minute: '2-digit',
})

// Niche table
const nicheRows = Object.entries(report.byNiche)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([niche, { pass, total }]) => {
    const pct = total === 0 ? '—' : `${Math.round((pass / total) * 100)}%`
    const status = pass === total ? '✅' : '❌'
    return `| ${status} ${niche} | ${pass} | ${total} | ${pct} |`
  })
  .join('\n')

// Failed assertions
const failedLines = report.failed.length === 0
  ? '_All assertions passed._'
  : report.failed
      .map(f => `- \`${f.id}\` (${f.niche}) — ${f.anti ? 'should NOT contain' : 'missing'}: \`${f.fragment}\``)
      .join('\n')

const passRate = report.total === 0 ? '—' : `${Math.round((report.passed / report.total) * 100)}%`
const badge = report.failed.length === 0 ? '🟢 ALL PASS' : `🔴 ${report.failed.length} FAILED`

const markdown = `# Call Scenario Test Results

${badge} — Last run: ${date}

| Metric | Value |
|--------|-------|
| Total assertions | ${report.total} |
| Passed | ${report.passed} |
| Failed | ${report.failed.length} |
| Pass rate | ${passRate} |

## By Niche

| Niche | Pass | Total | Rate |
|-------|------|-------|------|
${nicheRows}

## Failed Assertions

${failedLines}

---

_Generated by \`scripts/write-scenario-results.mjs\` — do not edit manually_
_Source: \`.test-artifacts/call-scenarios.json\`_
`

fs.mkdirSync(path.dirname(VAULT_PATH), { recursive: true })
fs.writeFileSync(VAULT_PATH, markdown)

console.log(`[write-scenario-results] Written to ${VAULT_PATH}`)
console.log(`  Total: ${report.total} | Passed: ${report.passed} | Failed: ${report.failed.length}`)

if (report.failed.length > 0) {
  console.log('\n  Failed:')
  for (const f of report.failed) {
    console.log(`    ❌ ${f.id} (${f.niche}) — ${f.anti ? 'should NOT contain' : 'missing'}: "${f.fragment}"`)
  }
  process.exit(1)
}
